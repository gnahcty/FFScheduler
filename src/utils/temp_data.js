/**
 *
 * @returns {Array} - Return an array of categories
 */
export const getCategories = () => {
  const arr = []
  data.map((item) => {
    if (!arr.find((el) => el.name === item.category)) {
      arr.push({ name: item.category, photos: item.photos })
    }
  })
  return arr
}

/**
 *
 * @returns {Array} - Return an array of films in that category
 */
export const categoryList = () => {
  const arr = []
  data.map((item) => {
    if (!arr.find((el) => el === item.category)) {
      arr.push(item.category)
    }
  })
  return arr
}

/**
 *
 * @param {string} category
 * @returns {Array} - Return an array of films in that category
 */
export const getFilmsByCategory = (category) => {
  return data.filter((item) => item.category === category)
}

